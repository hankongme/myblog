<script>

    function submitForms() {
        var name = $('#name').val();
        if(!name){
            layer.msg("请填写账号!");
            return false;
        }

        var pass = $('#pass').val();
        if(!pass){
            layer.msg("请填写密码!");
            return false;
        }

        $.ajax({
            cache: true,
            type: "POST",
            url: "{{url(__ADMIN_PATH__.'/login')}}",
            data: $('#login_form').serialize(),
            async: false,
            error: function (request) {
                layer.msg("网络错误,请稍后重试");
                init_geetest.reset();
            },
            success: function (data) {
                if (data.status == 0) {
                    layer.msg(data.return);
                    window.location.href = data.url;
                } else {
                    layer.msg(data.return);
                    init_geetest.reset();
                }
            }
        });

    }

</script>
